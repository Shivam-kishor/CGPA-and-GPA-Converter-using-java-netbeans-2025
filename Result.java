    private void CalculateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateBtnActionPerformed
double totGPoint;
double totCHour;
double gpa;
double cgpa;

double S1=Double.parseDouble(S1cgpaTF.getText());
double c1=Double.parseDouble(C1gpTF.getText());
double c2=Double.parseDouble(C2gpTF.getText());
double c3=Double.parseDouble(C3gpTF.getText());
double c4=Double.parseDouble(C4gpTF.getText());
double c5=Double.parseDouble(C5gpTF.getText());
double c6=Double.parseDouble(C6gpTF.getText());

double ch1=Double.parseDouble(C1chTF.getText());
double ch2=Double.parseDouble(C2chTF.getText());
double ch3=Double.parseDouble(C3chTF.getText());
double ch4=Double.parseDouble(C4chTF.getText());
double ch5=Double.parseDouble(C5chTF.getText());
double ch6=Double.parseDouble(C6chTF.getText());// TODO add your handling code here:

totGPoint=(c1*ch1)+(c2*ch2)+(c3*ch3)+(c4*ch4)+(c5*ch5)+(c6*ch6);
totCHour=ch1+ch2+ch3+ch4+ch5+ch6;
gpa=totGPoint/totCHour;
cgpa=(S1+gpa)/2;      

if(cgpaRad.isSelected()){
        cgpaTF.setText(""+cgpa);
    }
else if(gpaRad.isSelected()){
        gpaTF.setText(""+gpa);
    }
     
    }//GEN-LAST:event_CalculateBtnActionPerformed

